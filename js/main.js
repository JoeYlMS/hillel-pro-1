let elCreate = function (object, tag, [...key], path, style = "") {
    let x = ``;
    key.forEach((item) => {
        x += item + ": " + object[item] + "; ";
    });
    let box = document.createElement(tag);
    box.textContent = x;
    box.classList = style;
    path.appendChild(box);
}

