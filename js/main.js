function checkInput(arr) {
    arr.forEach((elem) => {
        if (elem.value === "") {
            elem.classList.add('check_color')
        } else {
            elem.classList.remove('check_color')
        }
    })
    for (const arrElement of arr) {
        if (arrElement.value === "") {
            return false
        }
    }
    return true
}

