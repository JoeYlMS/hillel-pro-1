function btnCreate  (tag, str, path, style)  {
    let btn = document.createElement(tag);
    btn.innerHTML = str;
    btn.classList = style;
    path.append(btn);
}

function clearInput(arr){
    arr.forEach((el)=>{
        el.value = ''
    })
}